# class 
class Employee:
    name = "Default"
    age = 0
    student = False

Rajat = Employee()
Rajat.name = "Rajat"
Rajat.age = 19
Rajat.student = True
print("Name:",Rajat.name)
print("age:",Rajat.age)
print("student:",Rajat.student)
 
isha = Employee()
isha.name = "Isha"
isha.age = 23
isha.student = False
print("Name:",isha.name)
print("age:",isha.age)
print("student:",isha.student)
 

