'''| Operator | Example   | Same As      |
| -------- | --------- | ------------ |
| `=`      | `x = 5`   | x is 5       |
| `+=`     | `x += 2`  | `x = x + 2`  |
| `-=`     | `x -= 2`  | `x = x - 2`  |
| `*=`     | `x *= 3`  | `x = x * 3`  |
| `/=`     | `x /= 2`  | `x = x / 2`  |
| `//=`    | `x //= 2` | `x = x // 2` |
| `%=`     | `x %= 2`  | `x = x % 2`  |
| `**=`    | `x **= 2` | `x = x ** 2` |
'''