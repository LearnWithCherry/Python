a = 5
b = 10

print(a+b)           #  15
print(a/b)           #  0.5
print(a-b)           #  -5
print(a*b)           #  50
print(a//b)          #  0
print(a%b)           #  5
print(a**b)          #  9765625


'''| Operator | Description         | Example          |
| -------- | ------------------- | ---------------- |
| `+`      | Addition            | `5 + 3` → `8`    |
| `-`      | Subtraction         | `5 - 2` → `3`    |
| `*`      | Multiplication      | `4 * 2` → `8`    |
| `/`      | Division            | `10 / 2` → `5.0` |
| `//`     | Floor Division      | `10 // 3` → `3`  |
| `%`      | Modulus (remainder) | `10 % 3` → `1`   |
| `**`     | Exponentiation      | `2 ** 3` → `8`   |
'''
